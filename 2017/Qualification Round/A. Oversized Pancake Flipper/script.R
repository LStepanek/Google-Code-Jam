## I am loading input data ----------------------------------------------------

my_input <- file.choose()
my_data <- readLines(my_input)


## ----------------------------------------------------------------------------

###############################################################################

## I am changing a working directory ------------------------------------------

setwd(
    gsub("(.*\\\\)(.*)", "\\1", my_input)
)


## ----------------------------------------------------------------------------

###############################################################################

## helper functions -----------------------------------------------------------

useFlipper <- function(my_pancakes, my_indices){
    
    # '''
    # Switches exactly K pancakes in the row with indices "my_indices"
    # '''
    
    output <- my_pancakes
    
    for(i in my_indices){
        
        if(my_pancakes[i] == "+"){output[i] <- "-"}else{output[i] <- "+"}
        
    }
    
    return(output)    
    
}


solveTheCase <- function(
    
    my_case
    
){
    
    # '''
    # Returns a solution for one case of the input data.
    # '''
    
    S <- strsplit(strsplit(my_case, split = " ")[[1]][1], split = "")[[1]]
    K <- as.integer(strsplit(my_case, split = " ")[[1]][2])
    
    output <- 0
    
    for(i in 1:(length(S) - K + 1)){
        
        if(S[i] == "-"){
            
            S <- useFlipper(
                S, c(i:(i + K - 1))
            )
            
            output <- output + 1
                    
        }
        
    }
    
    if(! "-" %in% S[(length(S) - K + 1):length(S)]){
        return(output)
    }else{
        return("IMPOSSIBLE")
    }
    
}


## ----------------------------------------------------------------------------

###############################################################################

## flow control ---------------------------------------------------------------

if(
    as.integer(my_data[1]) != length(my_data) - 1
){
    stop("Number of cases is fishy.", call. = FALSE)
}


## ----------------------------------------------------------------------------

###############################################################################

## core computations ----------------------------------------------------------

my_output <- NULL

for(i in 1:as.integer(my_data[1])){

    my_case <- my_data[i + 1]

    my_output <- c(
        my_output,
        paste(
            "Case #",
            i,
            ": ",
            solveTheCase(my_case),
            sep = ""
        )
    )
    
    flush.console()
    print(
        paste(
            "Process is ",
            format(
                round(
                    i / as.integer(my_data[1]) * 100,
                    digits = 2
                ),
                nsmall = 2
            ),
            " % complete.",
            sep = ""            
        )
    )
    
}


## ----------------------------------------------------------------------------

###############################################################################

## I am giving an output ------------------------------------------------------

writeLines(
    my_output,
    con = gsub(
        ".in",
        "-out.in",
        gsub("(.*\\\\)(.*)", "\\2", my_input)
    )
)


